    private void btnBurcarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBurcarActionPerformed
        // TODO add your handling code here:
        String serialCarro = this.txtBuscar.getText();
        if(listaCarro.containsKey(serialCarro)){
            Carro carro = listaCarro.get(serialCarro);
            this.txtColor.setText(carro.getColor());
            this.txtMarca.setText(carro.getMarca());
            this.txtSerial.setText(String.valueOf(carro.getSerial()));
            this.txtModelo.setText(String.valueOf(carro.getModelo()));
            this.txtSerial.setEditable(false);
        }else{
            JOptionPane.showConfirmDialog(null,"Error, el carro no exite");
            this.btnBurcar.setText("");
        }
    }//GEN-LAST:event_btnBurcarActionPerformed

