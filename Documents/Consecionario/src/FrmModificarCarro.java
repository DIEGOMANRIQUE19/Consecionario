    private void btnModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarActionPerformed
        Carro nuevoCarro =  new Carro();
        nuevoCarro.setColor(this.txtColor.getText());
        nuevoCarro.setMarca(this.txtMarca.getText());
        nuevoCarro.setModelo(Integer.parseInt(this.txtModelo.getText()));
        nuevoCarro.setSerial(Integer.parseInt(this.txtSerial.getText()));
        listaCarro.put(this.txtSerial.getText(),nuevoCarro);
        JOptionPane.showMessageDialog(null,"Carro Modificado");
                this.btnLimpiar.doClick();
    }//GEN-LAST:event_btnModificarActionPerformed

